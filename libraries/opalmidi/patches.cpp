// ----------------------------------------------------------------------------
bool OPLPatch::load(OPLPatchSet& patches, const uint8_t *data, size_t size)
{
	return loadWOPL(patches, data, size)
	    || loadOP2(patches, data, size)
	    || loadAIL(patches, data, size)
	    || loadTMB(patches, data, size);
}

